void main() {
  int a = 20;
  int b = 10;

  // tipo binario suma
  print(a + b);
  // tipo binario resta
  print(a - b);
  // tipo binaria multiplicacion
  print(a * b);

  int c = 30;
  int d = 14;
  // tipo binaria division
  print(c / d);
  // redondear a entero la division ~
  print(c ~/ d);
  // reciduo
  print(a % b);
}
